#!/usr/bin/env python3
"""
install_adapters.py — L1 distribution installer (PAICodeConstitution-2026).

Derives the canonical hooks block from the workspace-root .claude/settings.json
(the single policy source), then writes / checks <project>/.claude/settings.json
for every MIGRATED project (a dir containing NEOCORTEX/ within maxdepth 3 of
the workspace root). Enforcement follows migration: unmigrated BRAIN-only
projects are NOT armed until they migrate (NEOCORTEX_SPEC §5).

Modes:
  (default / --dry-run)  Print a unified diff per project; write nothing.
  --apply                Write all adapters; update INSTALLED.json manifest.
  --check                Compare each installed adapter against the canonical source;
                         exit nonzero + list any drifted or missing project.

Art. 4 dry-run gate: --apply is explicit opt-in.
Idempotent: re-running --apply after --apply produces zero changes.

Usage:
  python3 install_adapters.py              # dry-run (default)
  python3 install_adapters.py --apply      # write adapters + INSTALLED.json
  python3 install_adapters.py --check      # drift-check; exit nonzero on drift
"""

import argparse
import difflib
import hashlib
import json
import os
import pathlib
import stat
import sys
from datetime import datetime, timezone

# ── Constants ────────────────────────────────────────────────────────────────

SCRIPT_DIR = pathlib.Path(__file__).resolve().parent            # governance/adapters/claude-code/
ROOT = (SCRIPT_DIR / "../../..").resolve()                      # workspace root

CANONICAL_SETTINGS = ROOT / ".claude" / "settings.json"
HOOKS_DIR          = ROOT / ".claude" / "hooks"
INSTALLED_JSON     = ROOT / "governance" / "adapters" / "INSTALLED.json"

# A project is ARMED when it has a NEOCORTEX/ at depth ≤ 3 relative to ROOT —
# i.e. when it has been migrated. Enforcement follows migration (NEOCORTEX_SPEC
# §5); unmigrated BRAIN-only projects are intentionally NOT discovered here.
STATE_DIR_NAMES = {"NEOCORTEX"}

GENERATED_COMMENT = (
    "L1 enforcement adapter — generated by governance/adapters/claude-code/"
    "install_adapters.py from the workspace canonical source "
    "(.claude/settings.json). LOCAL-ONLY, DO NOT EDIT BY HAND — re-run the "
    "installer to update. Policy: see your regime's POLICY_CORE.md. "
    "Generated state, gitignored per Constitution Art. 5."
)

MAX_DEPTH = 3


# ── Helpers ──────────────────────────────────────────────────────────────────

def _sha256(text: str) -> str:
    return hashlib.sha256(text.encode()).hexdigest()


def _safe_confined_write(anchor: pathlib.Path, target_dir: pathlib.Path,
                         filename: str, content: bytes,
                         exclusive: bool = False) -> None:
    """
    Write <target_dir>/<filename> WITHOUT following any symlink in the path from
    <anchor> down to and including the final file, and WITHOUT truncating, writing
    through, or following a symlink / hardlink / FIFO planted as the final name.

    <anchor> MUST be a trusted, already-resolved (symlink-free) root. The path is
    walked component-by-component with openat()+O_NOFOLLOW, so a symlink anywhere
    in the chain — an INTERMEDIATE directory just as much as the final component —
    raises OSError and aborts the write (closing the full path-traversal TOCTOU;
    O_NOFOLLOW on a single open only guards the last component). Missing directory
    components are created via mkdir() relative to the pinned parent fd.

    The final write avoids the O_TRUNC-at-open clobber: O_TRUNC truncates BEFORE
    any fstat can run, so a hardlink planted as <filename> (which O_NOFOLLOW does
    NOT catch and which IS a regular file, so an S_ISREG check passes) would have
    its external victim inode emptied. Instead:
      - exclusive=True  → O_CREAT|O_EXCL (atomic create-or-skip; refuses ANY
        existing node — regular/symlink/hardlink/FIFO — with FileExistsError).
      - exclusive=False → write a fresh O_EXCL temp in the pinned dir, then
        renameat() it over <filename>. rename replaces only the directory entry;
        it never follows/truncates/writes-through a planted node, so no victim
        inode can be clobbered and there is no race window.
    O_NONBLOCK + S_ISREG + st_nlink==1 refuse a FIFO/device/hardlink target.
    """
    rel_parts = target_dir.relative_to(anchor).parts
    if not rel_parts:
        raise OSError("refusing to write directly into the trusted anchor")

    # anchor is .resolve()'d (canonical, symlink-free) — the trust root.
    dir_fd = os.open(str(anchor), os.O_RDONLY | os.O_DIRECTORY)
    try:
        for part in rel_parts:
            try:
                next_fd = os.open(
                    part, os.O_RDONLY | os.O_DIRECTORY | os.O_NOFOLLOW, dir_fd=dir_fd
                )
            except FileNotFoundError:
                # Create the missing component relative to the pinned parent fd,
                # then re-open it with O_NOFOLLOW (a race-swap to a symlink in the
                # gap makes this re-open fail closed).
                os.mkdir(part, 0o755, dir_fd=dir_fd)
                next_fd = os.open(
                    part, os.O_RDONLY | os.O_DIRECTORY | os.O_NOFOLLOW, dir_fd=dir_fd
                )
            os.close(dir_fd)
            dir_fd = next_fd
        # dir_fd now pins the real target_dir, reached without traversing a symlink.
        if exclusive:
            # Atomic create-or-skip: O_EXCL refuses ANY existing node (regular file,
            # symlink, hardlink, FIFO, dir) → FileExistsError (caller: "already there,
            # skip"). A fresh O_EXCL inode is regular + single-link by construction.
            file_fd = os.open(
                filename,
                os.O_WRONLY | os.O_CREAT | os.O_EXCL | os.O_NOFOLLOW | os.O_NONBLOCK,
                0o644, dir_fd=dir_fd,
            )
            try:
                st = os.fstat(file_fd)
                if not stat.S_ISREG(st.st_mode) or st.st_nlink != 1:
                    raise OSError(f"{filename} is not a fresh regular file; refusing to write.")
                os.write(file_fd, content)
            finally:
                os.close(file_fd)
        else:
            # Refuse to overwrite anything that is not already a plain single-link
            # regular file: a symlink / hardlink / FIFO / device planted as <filename>
            # is skipped (OSError), not silently replaced. lstat() does not follow it.
            # Best-effort gate: a post-lstat race is still SAFE because the write goes
            # to a fresh temp + renameat (below), never opening/truncating the entry.
            try:
                est = os.lstat(filename, dir_fd=dir_fd)
            except FileNotFoundError:
                est = None
            if est is not None and (not stat.S_ISREG(est.st_mode) or est.st_nlink != 1):
                raise OSError(f"{filename} is not a plain regular file; refusing to overwrite.")
            # Atomic overwrite via fresh-temp + renameat: never opens/truncates the
            # existing entry, so even a node raced in after the lstat cannot be
            # followed, truncated (the O_TRUNC-at-open clobber), or written through.
            # renameat replaces only the directory entry (relative to the pinned dir-fd).
            tmp = f".{filename}.{os.urandom(8).hex()}.tmp"
            file_fd = os.open(
                tmp,
                os.O_WRONLY | os.O_CREAT | os.O_EXCL | os.O_NOFOLLOW | os.O_NONBLOCK,
                0o644, dir_fd=dir_fd,
            )
            try:
                try:
                    st = os.fstat(file_fd)
                    if not stat.S_ISREG(st.st_mode) or st.st_nlink != 1:
                        raise OSError("temp file is not a fresh regular file; refusing to write.")
                    os.write(file_fd, content)
                finally:
                    os.close(file_fd)
                os.rename(tmp, filename, src_dir_fd=dir_fd, dst_dir_fd=dir_fd)
            except BaseException:
                # any failure (fstat / write / rename) → remove the temp; never leak litter
                try:
                    os.unlink(tmp, dir_fd=dir_fd)
                except OSError:
                    pass
                raise
    finally:
        os.close(dir_fd)


def _safe_confined_read(anchor: pathlib.Path, target_dir: pathlib.Path,
                        filename: str):
    """
    Read <target_dir>/<filename> WITHOUT following any symlink in the path from
    <anchor> (trusted, already-resolved) down to and including the final file.

    Mirror of _safe_confined_write: walks every component with openat()+O_NOFOLLOW.
    Returns the file's UTF-8 text, or None if the file (or its .claude parent) is
    absent. Raises OSError if any component — intermediate dir or the final file —
    is a symlink/unsafe, so a read cannot be raced into following a symlink to an
    external file (a TOCTOU information-disclosure via the printed diff). The
    caller treats OSError as SKIP, exactly like the write path.
    """
    rel_parts = target_dir.relative_to(anchor).parts
    if not rel_parts:
        raise OSError("refusing to read directly from the trusted anchor")

    dir_fd = os.open(str(anchor), os.O_RDONLY | os.O_DIRECTORY)
    try:
        for part in rel_parts:
            try:
                next_fd = os.open(
                    part, os.O_RDONLY | os.O_DIRECTORY | os.O_NOFOLLOW, dir_fd=dir_fd
                )
            except FileNotFoundError:
                return None  # .claude (or an intermediate) absent → no existing adapter
            os.close(dir_fd)
            dir_fd = next_fd
        try:
            # O_NONBLOCK so a FIFO/device planted as settings.json cannot make the
            # open block forever (a local DoS); the S_ISREG check then refuses any
            # non-regular file outright. O_NONBLOCK is a no-op for regular files.
            file_fd = os.open(
                filename, os.O_RDONLY | os.O_NOFOLLOW | os.O_NONBLOCK, dir_fd=dir_fd
            )
        except FileNotFoundError:
            return None
        try:
            st = os.fstat(file_fd)
            if not stat.S_ISREG(st.st_mode) or st.st_nlink != 1:
                # st_nlink>1 ⇒ a hardlink: O_NOFOLLOW does not catch it and it IS a
                # regular file, so without this check its (external) bytes would leak
                # into the printed diff. Refuse it.
                raise OSError(f"{filename} is not a regular single-link file; refusing to read.")
            chunks = []
            while True:
                buf = os.read(file_fd, 65536)
                if not buf:
                    break
                chunks.append(buf)
            return b"".join(chunks).decode("utf-8")
        finally:
            os.close(file_fd)
    finally:
        os.close(dir_fd)


def _load_canonical() -> dict:
    """Load and return the hooks block from the workspace-root settings.json."""
    if not CANONICAL_SETTINGS.exists():
        sys.exit(f"ERROR: canonical settings not found: {CANONICAL_SETTINGS}")
    with CANONICAL_SETTINGS.open() as f:
        data = json.load(f)
    if "hooks" not in data:
        sys.exit(f"ERROR: no 'hooks' key in {CANONICAL_SETTINGS}")
    return data["hooks"]


def _hooks_with_absolute_paths(hooks_block: dict) -> dict:
    """
    Return a deep-copy of the hooks block with all command strings resolved to
    absolute paths under HOOKS_DIR.

    The canonical source uses "$CLAUDE_PROJECT_DIR"/.claude/hooks/... — we
    replace the variable prefix with the absolute path to HOOKS_DIR.
    """
    import copy, re
    block = copy.deepcopy(hooks_block)

    def fix_cmd(cmd: str) -> str:
        # Matches both quoted and unquoted $CLAUDE_PROJECT_DIR prefix variants
        # plus any relative ./.claude/hooks/ reference.
        cmd = re.sub(
            r'"?\$CLAUDE_PROJECT_DIR"?/\.claude/hooks/',
            str(HOOKS_DIR) + "/",
            cmd,
        )
        # Already absolute (hand-copied adapters) — keep as-is
        return cmd

    for event_hooks in block.values():
        for entry in event_hooks:
            for hook in entry.get("hooks", []):
                if "command" in hook:
                    hook["command"] = fix_cmd(hook["command"])
    return block


def _build_adapter_content(hooks_block: dict) -> str:
    """Return the JSON string for a project adapter settings.json."""
    payload = {
        "_generated_by": GENERATED_COMMENT,
        "hooks": hooks_block,
    }
    return json.dumps(payload, indent=2) + "\n"


def _discover_projects() -> list[pathlib.Path]:
    """
    Return a sorted list of unique project root dirs (parent of NEOCORTEX/ or
    BRAIN/) found within maxdepth MAX_DEPTH of ROOT, excluding ROOT itself
    and any governance/ or .claude/ subtrees.
    """
    found: set[pathlib.Path] = set()

    def _scan(current: pathlib.Path, depth: int):
        if depth > MAX_DEPTH:
            return
        try:
            entries = list(current.iterdir())
        except PermissionError:
            return
        for entry in entries:
            if not entry.is_dir():
                continue
            # Skip hidden dirs (except we already handled .claude at root),
            # and skip governance/ to avoid false positives.
            if entry.name.startswith("."):
                continue
            if entry == ROOT / "governance":
                continue
            if entry.name in STATE_DIR_NAMES:
                found.add(current)  # current is the project root
            else:
                _scan(entry, depth + 1)

    _scan(ROOT, 0)
    return sorted(found)


def _read_installed() -> list[dict]:
    if INSTALLED_JSON.exists():
        with INSTALLED_JSON.open() as f:
            return json.load(f)
    return []


def _write_installed(records: list[dict]):
    INSTALLED_JSON.parent.mkdir(parents=True, exist_ok=True)
    with INSTALLED_JSON.open("w") as f:
        f.write(json.dumps(records, indent=2) + "\n")


def _now_iso() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _make_record(project: pathlib.Path, has_adapter: bool,
                 policy_hash: str, action: str) -> dict:
    return {
        "project_path": str(project),
        "has_adapter": has_adapter,
        "policy_source_hash": policy_hash,
        "last_action": action,
        "updated": _now_iso(),
    }


# ── Mode implementations ──────────────────────────────────────────────────────

def _mode_dry_run_or_apply(apply: bool) -> int:
    canonical_hooks = _load_canonical()
    abs_hooks = _hooks_with_absolute_paths(canonical_hooks)
    desired_content = _build_adapter_content(abs_hooks)
    policy_hash = _sha256(json.dumps(canonical_hooks, sort_keys=True))

    projects = _discover_projects()
    print(f"Discovered {len(projects)} project(s) with state dirs.\n")

    records: list[dict] = []
    changed = 0
    unchanged = 0

    for proj in projects:
        adapter_path = proj / ".claude" / "settings.json"
        dot_claude = proj / ".claude"

        # Symlink guard: refuse to read/write through symlinks to prevent
        # path-traversal into unexpected directories.
        # NOTE: broken symlinks have exists()==False but is_symlink()==True —
        # omit the exists() short-circuit so broken symlinks are caught too.
        if dot_claude.is_symlink() or adapter_path.is_symlink():
            print(f"  SKIP (symlink)  {proj.relative_to(ROOT)} — "
                  f".claude/ or settings.json is a symlink; refusing to touch.")
            continue
        # Confinement check: resolved adapter_path must stay inside proj.
        # Runs unconditionally so a broken symlink whose target escapes the
        # project is also caught (resolve() follows the dangling link path).
        try:
            adapter_path.resolve().relative_to(proj.resolve())
        except ValueError:
            print(f"  SKIP (escape)  {proj.relative_to(ROOT)} — "
                  f"adapter_path resolves outside project dir; refusing to touch.")
            continue

        # Symlink-safe read (mirror of the confined write): a race that swaps
        # settings.json for a symlink after the guard above would otherwise leak
        # an external file's bytes into the printed diff. OSError → SKIP.
        try:
            existing = _safe_confined_read(ROOT, dot_claude, "settings.json")
        except OSError as e:
            print(f"  SKIP (unsafe path)  {proj.relative_to(ROOT)} — "
                  f"symlink/unsafe component in path on read; refusing to touch ({e}).")
            continue

        if existing is not None and existing == desired_content:
            status = "UNCHANGED"
            unchanged += 1
            action = "unchanged"
            records.append(_make_record(proj, True, policy_hash, action))
            if not apply:
                print(f"  {status}  {proj.relative_to(ROOT)}")
            continue

        # There is a diff to show / apply
        changed += 1
        from_lines = existing.splitlines(keepends=True) if existing else []
        to_lines   = desired_content.splitlines(keepends=True)
        from_name  = str(adapter_path.relative_to(ROOT)) if existing else "(absent)"
        to_name    = str(adapter_path.relative_to(ROOT))

        diff = list(difflib.unified_diff(
            from_lines, to_lines,
            fromfile=from_name, tofile=to_name,
        ))

        print(f"  {'WOULD WRITE' if not apply else 'WRITING'}  {proj.relative_to(ROOT)}")
        if diff:
            print("".join(diff[:60]))  # cap diff output at 60 lines per project
        else:
            print("  (new file — no diff)")

        if apply:
            # Full path-traversal hardening: walk ROOT → … → proj/.claude with
            # openat()+O_NOFOLLOW at EVERY component (intermediate dirs AND the
            # final settings.json), creating missing dirs relative to pinned fds.
            # A symlink swapped in anywhere along the path — not just the final
            # component — is refused, closing the intermediate-/parent-directory
            # TOCTOU that O_NOFOLLOW-on-the-final-open alone could not.
            try:
                _safe_confined_write(
                    ROOT, dot_claude, "settings.json",
                    desired_content.encode("utf-8"),
                )
            except OSError as e:
                print(f"  SKIP (unsafe path)  {proj.relative_to(ROOT)} — "
                      f"symlink/unsafe component in path; refusing to write ({e}).")
                continue
            action = "written"
        else:
            action = "pending"

        records.append(_make_record(proj, apply, policy_hash, action))

    if apply:
        if changed > 0:
            _write_installed(records)
            print(f"\nINSTALLED.json updated ({len(records)} entries).")
        else:
            # Nothing changed — preserve existing INSTALLED.json timestamps
            print(f"\nNo-op: all {unchanged} project(s) already current. "
                  f"INSTALLED.json unchanged.")
    else:
        print(f"\nDry-run summary: {changed} would change, {unchanged} already current.")
        print("Pass --apply to write.")

    return 0


def _mode_check() -> int:
    """Drift-check: compare each installed adapter to the canonical source."""
    canonical_hooks = _load_canonical()
    abs_hooks = _hooks_with_absolute_paths(canonical_hooks)
    desired_content = _build_adapter_content(abs_hooks)
    policy_hash = _sha256(json.dumps(canonical_hooks, sort_keys=True))

    projects = _discover_projects()
    drifted: list[str] = []
    missing: list[str] = []
    clean:   list[str] = []

    for proj in projects:
        adapter_path = proj / ".claude" / "settings.json"
        rel = str(proj.relative_to(ROOT))
        dot_claude = proj / ".claude"

        # Symlink guard (check mode): skip symlinked .claude dirs / settings.
        # NOTE: broken symlinks have exists()==False but is_symlink()==True —
        # omit the exists() short-circuit so broken symlinks are caught too.
        if dot_claude.is_symlink() or adapter_path.is_symlink():
            print(f"  SKIP (symlink)  {rel} — "
                  f".claude/ or settings.json is a symlink; skipping check.")
            continue
        # Confinement check — unconditional (catches broken-symlink escapes too).
        try:
            adapter_path.resolve().relative_to(proj.resolve())
        except ValueError:
            print(f"  SKIP (escape)  {rel} — "
                  f"adapter_path resolves outside project dir; skipping check.")
            continue

        # Symlink-safe read (mirror of apply): refuse a raced symlink swap.
        try:
            existing = _safe_confined_read(ROOT, dot_claude, "settings.json")
        except OSError:
            print(f"  SKIP (symlink)  {rel} — "
                  f"symlink/unsafe component in path on read; skipping check.")
            continue
        if existing is None:
            missing.append(rel)
            continue
        if existing == desired_content:
            clean.append(rel)
        else:
            drifted.append(rel)

    print(f"Checked {len(projects)} project(s).\n")

    if clean:
        for r in clean:
            print(f"  OK     {r}")

    if missing:
        for r in missing:
            print(f"  MISSING  {r}")

    if drifted:
        for r in drifted:
            print(f"  DRIFTED  {r}")

    if missing or drifted:
        print(f"\nCHECK FAILED: {len(missing)} missing, {len(drifted)} drifted.")
        return 1

    print(f"\nCHECK PASSED: all {len(clean)} armed projects clean (0 drift).")
    return 0


# ── Entry point ───────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="L1 adapter installer — governance/adapters/claude-code/"
    )
    group = parser.add_mutually_exclusive_group()
    group.add_argument("--apply",    action="store_true",
                       help="Write adapters and update INSTALLED.json (default is dry-run).")
    group.add_argument("--check",    action="store_true",
                       help="Drift-check only; exit nonzero on missing/drifted projects.")
    group.add_argument("--dry-run",  action="store_true",
                       help="Show diffs without writing (this is the default).")
    args = parser.parse_args()

    if args.check:
        sys.exit(_mode_check())
    else:
        # --dry-run or no flag both invoke dry-run; --apply actually writes
        sys.exit(_mode_dry_run_or_apply(apply=args.apply))


if __name__ == "__main__":
    main()
